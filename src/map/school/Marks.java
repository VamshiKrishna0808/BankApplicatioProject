package map.school;

public class Marks {
	
	private int total;
	private int markEnglish;
	private int markMaths;
	private int markScience;
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getMarkEnglish() {
		return markEnglish;
	}
	public void setMarkEnglish(int markEnglish) {
		this.markEnglish = markEnglish;
	}
	public int getMarkMaths() {
		return markMaths;
	}
	public void setMarkMaths(int markMaths) {
		this.markMaths = markMaths;
	}
	public int getMarkScience() {
		return markScience;
	}
	public void setMarkScience(int markScience) {
		this.markScience = markScience;
	}
	
}
