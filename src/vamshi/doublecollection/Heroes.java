package vamshi.doublecollection;

import java.util.List;

public class Heroes {

	String heroName;
	
	List<Movies> movies;
	
	List<Serials> serial;

	public String getHeroName() {
		return heroName;
	}

	public void setHeroName(String heroName) {
		this.heroName = heroName;
	}

	public List<Movies> getMovies() {
		return movies;
	}

	public void setMovies(List<Movies> movies) {
		this.movies = movies;
	}

	public List<Serials> getSerial() {
		return serial;
	}

	public void setSerial(List<Serials> serial) {
		this.serial = serial;
	}
	
	
	
	
}
